#include "ft_printf.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*copy;

	copy = ft_strnew(ft_strlen(s));
	if (!copy)
		return (NULL);
	i = 0;
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = s[i];
	return (copy);
}
